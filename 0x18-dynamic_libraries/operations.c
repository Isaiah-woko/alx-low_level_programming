#include <stdio.h>

/**
 * add - function to add two integers
 * @a: first integer
 * @b: second integer
 * Return: the result of the operation
*/

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function to subtract two integers
 * @a: first integer
 * @b: second integer
 * Return: the result of the operation
*/
int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - function to multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: the result of the operation
*/
int mul(int a, int b)
{
	return (a * b);
}


/**
 * div - function to divide two integers
 * @a: first integer
 * @b: second integer
 * Return: the result of the operation
*/
int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - function to mod two integers
 * @a: first integer
 * @b: second integer
 * Return: the result of the operation
*/
int mod(int a, int b)
{
	return (a % b);
}
